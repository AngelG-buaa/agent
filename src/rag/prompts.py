CITATION_GUIDELINES = """\
# 引用规范
在回答中引用知识库内容时，请严格遵守以下规则：

## 引用格式
- 使用 [ID:n] 标注来源，多个来源时写 [ID:n][ID:m]
- 引用放在相关句子的末尾、标点符号之前
- 每句最多引用 4 个来源

## 必须引用的情况
- 数据、统计数字、百分比
- 日期、时间线、事件顺序
- 因果关系声明
- 比较、排名、对比声明
- 技术术语定义、方法论
- 直接引语、他人的观点
- 预测、趋势分析
- 有争议的事实

## 不应引用的情况
- 常识（如"太阳从东边升起"）
- 过渡语句
- 你自己的分析和综合
- 工具返回中不存在的 [ID:n]（严禁编造）"""